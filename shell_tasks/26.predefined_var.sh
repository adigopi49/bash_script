$0: The name of the script or shell itself.
$1, $2, ...: The positional parameters, where $1 represents the first argument passed to the script, $2 represents the second argument, and so on.
$@: All the positional parameters passed to the script as an array.
$#: The number of positional parameters passed to the script.
$?: The exit status of the last command executed.
$$: The process ID (PID) of the current shell.
$!: The PID of the last background command.
$USER: The username of the current user.
$HOME: The home directory of the current user.
$PWD: The present working directory.
$SHELL: The path to the current shell.
$HOSTNAME: The hostname of the system.
$HOSTTYPE: The type of the host system (e.g., x86_64).
$OSTYPE: The operating system type (e.g., linux-gnu).
$PATH: The list of directories where the shell looks for executable files.
$IFS: The Internal Field Separator, used for word splitting.
$LINENO: The current line number in the script.:wq
